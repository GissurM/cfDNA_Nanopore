    stats_text += f"PC1-Depth: r={results['pc1_depth_correlation']:.3f} {pc1_sig}\n"
    stats_text += f"PC2-Depth: r={results['pc2_depth_correlation']:.3f} {pc2_sig}\n\n"

    if results["stats_results"]:
        stats_text += "Group Separation Tests:\n"
        for pc, stats in results["stats_results"].items():
            sig_marker = "***" if stats["p_value"] < 0.001 else "**" if stats["p_value"] < 0.01 else "*" if stats["p_value"] < 0.05 else ""
            stats_text += f"{pc} ({stats.get('test', 'mwu')}): p={stats['p_value']:.4f} {sig_marker}\n"

    ax7.text(0.05, 0.95, stats_text, transform=ax7.transAxes, fontsize=11, verticalalignment="top", fontfamily="monospace")

    ax8 = plt.subplot(3, 3, 8)
    if "PC1" in results["feature_contributions"]:
        contrib_data = results["feature_contributions"]["PC1"]
        sizes = [c["size_bin"] for c in contrib_data]
        loadings = [c["loading"] for c in contrib_data]

        plt.bar(range(len(sizes)), loadings, color=["red" if x < 0 else "blue" for x in loadings], alpha=0.7)
        plt.xlabel("Top Size Bins")
        plt.ylabel("PC1 Loading")
        plt.title(f"{fragment_type} - PC1 Top Contributors")
        plt.xticks(range(len(sizes)), sizes, rotation=45)
        plt.axhline(y=0, color="black", linestyle="-", alpha=0.3)

    ax9 = plt.subplot(3, 3, 9)
    depths = results["sequencing_depths"]
    scatter = plt.scatter(
        results["X_pca"][:, 0],
        results["X_pca"][:, 1],
        c=depths,
        cmap="plasma",
        s=80,
        alpha=0.8,
        edgecolors="black",
        linewidth=0.5,
    )

    cbar = plt.colorbar(scatter, ax=ax9, shrink=0.8)
    cbar.set_label("Sequencing Depth (Total Fragments)", rotation=270, labelpad=20)
    if np.max(depths) >= 1000:
        cbar.ax.yaxis.set_major_formatter(plt.FuncFormatter(lambda x, p: f"{x / 1000:.1f}K"))

    plt.xlabel(f"PC1 ({results['explained_variance'][0]:.1%})")
    plt.ylabel(f"PC2 ({results['explained_variance'][1]:.1%})")
    plt.title(f"{fragment_type} - Sequencing Depth Heatmap")
