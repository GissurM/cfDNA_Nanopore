def parse_args():
    """Optional CLI overrides for paths/settings."""
    parser = argparse.ArgumentParser(description="cfDNA PCA analysis for detailed size bins")
    parser.add_argument("--data-dir", default=DATA_DIRECTORY, help="Input directory (recursive search)")
