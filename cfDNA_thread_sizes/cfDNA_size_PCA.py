def load_fragment_data(fragment_type):
    """
    Load detailed size distribution and count data for one fragment type.

    Returns:
        tuple: (percentage_df, count_df) or (None, None)
    """
    percentage_file = find_matching_file(DATA_DIRECTORY, fragment_type, "size_distributions.csv")
    count_file = find_matching_file(DATA_DIRECTORY, fragment_type, "size_counts.csv")

    if not percentage_file:
        print(f"Warning: percentage file not found for '{fragment_type}' in {DATA_DIRECTORY}")
        return None, None

    if not count_file:
        print(f"Warning: count file not found for '{fragment_type}' in {DATA_DIRECTORY}")
        return None, None

    try:
        percentage_df = pd.read_csv(percentage_file)
        count_df = pd.read_csv(count_file)

        print(f"Loaded {fragment_type} data:")
        print(f"  Percentage file: {percentage_file}")
        print(f"  Count file: {count_file}")
        print(f"  Percentage data: {percentage_df.shape}")
        print(f"  Count data: {count_df.shape}")

        size_columns = [col for col in percentage_df.columns if col.endswith("bp")]
        if size_columns:
            print(f"  Size columns: {len(size_columns)} ({size_columns[0]} to {size_columns[-1]})")
        else:
            print("  Warning: no columns ending with 'bp' were found")

        return percentage_df, count_df

    except Exception as e:
        print(f"Error loading {fragment_type} data: {e}")
        return None, None


def perform_fragment_pca(percentage_df, count_df, fragment_type):
    """
    Perform PCA analysis on detailed size bin data.

    Returns:
        dict: Results including PCA components, variance, stats, etc.
    """
    print(f"\n--- PCA Analysis: {fragment_type} ---")

    size_columns = [col for col in percentage_df.columns if col.endswith("bp")]
    if len(size_columns) < 3:
        print(f"Warning: only {len(size_columns)} size bins found for {fragment_type}")
        print("PCA requires at least 3 features for meaningful analysis")
        return None

    print(f"Using {len(size_columns)} size bin features")

    X = percentage_df[size_columns].values
    sample_names, sample_name_source = get_sample_names(percentage_df)
    groups, group_source = assign_groups(percentage_df, sample_names)

    size_values = np.array([int(col.replace("bp", "").split("_")[-1]) for col in size_columns])
    mean_fragment_sizes = np.average(X, axis=1, weights=size_values)

    unknown_mask = groups == "unknown"
    unknown_count = np.sum(unknown_mask)
    print(f"Assigned groups: {pd.Series(groups).value_counts().to_dict()}")
    print(f"Sample name source: {sample_name_source}; group source: {group_source}")
    if unknown_count > 0:
        print(f"Warning: {unknown_count} samples could not be assigned to a known group")

    if "total_fragments_all" in count_df.columns:
        sequencing_depths = count_df["total_fragments_all"].values
    elif "total_fragments" in count_df.columns:
        sequencing_depths = count_df["total_fragments"].values
    else:
        count_size_columns = [col for col in count_df.columns if col.endswith("bp")]
        sequencing_depths = np.sum(count_df[count_size_columns].values, axis=1)

    if DROP_UNKNOWN_GROUP and unknown_count > 0:
        keep_mask = ~unknown_mask
        if np.sum(keep_mask) < 4:
            print("Warning: not enough samples remain after dropping unknown groups")
            return None
        X = X[keep_mask]
        sample_names = sample_names[keep_mask]
        groups = groups[keep_mask]
        sequencing_depths = sequencing_depths[keep_mask]
        mean_fragment_sizes = mean_fragment_sizes[keep_mask]
        print(f"Dropped unknown samples. Remaining groups: {pd.Series(groups).value_counts().to_dict()}")

    if X.shape[0] < 4:
        print(f"Warning: only {X.shape[0]} samples - need at least 4 for PCA")
        return None

    column_std = np.std(X, axis=0)
    valid_columns = column_std > 1e-10
    if np.sum(valid_columns) < 2:
        print(f"Warning: only {np.sum(valid_columns)} columns with variance")
        return None

    X_filtered = X[:, valid_columns]
    size_columns_filtered = [size_columns[i] for i in range(len(size_columns)) if valid_columns[i]]
    print(f"After filtering: {X_filtered.shape[1]} features with variance")

    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X_filtered)

    n_components = min(X_scaled.shape[0] - 1, X_scaled.shape[1], 10)
    pca = PCA(n_components=n_components)
    X_pca = pca.fit_transform(X_scaled)

    explained_var = pca.explained_variance_ratio_
    cumulative_var = np.cumsum(explained_var)

    pc1_depth_corr, pc1_depth_p = pearsonr(X_pca[:, 0], sequencing_depths)
    pc2_depth_corr, pc2_depth_p = pearsonr(X_pca[:, 1], sequencing_depths)

    print(f"PC1 explains {explained_var[0]:.1%} of variance")
    print(f"PC2 explains {explained_var[1]:.1%} of variance")
    print(f"PC1+PC2 explains {cumulative_var[1]:.1%} of variance")

    group_types = np.unique(groups)
    stats_results = {}

    if len(group_types) == 2:
        for pc_idx in range(min(3, len(explained_var))):
            group1_data = X_pca[groups == group_types[0], pc_idx]
            group2_data = X_pca[groups == group_types[1], pc_idx]
            if len(group1_data) > 0 and len(group2_data) > 0:
                stat, p_val = mannwhitneyu(group1_data, group2_data, alternative="two-sided")
                stats_results[f"PC{pc_idx + 1}"] = {
                    "test": "mwu",
                    "statistic": stat,
                    "p_value": p_val,
                    "significant": p_val < 0.05,
                }
                sig = "***" if p_val < 0.001 else "**" if p_val < 0.01 else "*" if p_val < 0.05 else ""
                print(f"PC{pc_idx + 1} Mann-Whitney U test p-value: {p_val:.4f} {sig}")
    elif len(group_types) > 2:
        for pc_idx in range(min(3, len(explained_var))):
            per_group_data = [X_pca[groups == group_name, pc_idx] for group_name in group_types]
            if all(len(values) > 0 for values in per_group_data):
                stat, p_val = kruskal(*per_group_data)
                stats_results[f"PC{pc_idx + 1}"] = {
                    "test": "kruskal",
                    "statistic": stat,
                    "p_value": p_val,
                    "significant": p_val < 0.05,
                }
                sig = "***" if p_val < 0.001 else "**" if p_val < 0.01 else "*" if p_val < 0.05 else ""
                print(f"PC{pc_idx + 1} Kruskal-Wallis p-value: {p_val:.4f} {sig}")

    print("\nSequencing Depth Correlations:")
    pc1_sig = "***" if pc1_depth_p < 0.001 else "**" if pc1_depth_p < 0.01 else "*" if pc1_depth_p < 0.05 else ""
    pc2_sig = "***" if pc2_depth_p < 0.001 else "**" if pc2_depth_p < 0.01 else "*" if pc2_depth_p < 0.05 else ""
    print(f"PC1 vs Depth: r = {pc1_depth_corr:.3f}, p = {pc1_depth_p:.4f} {pc1_sig}")
    print(f"PC2 vs Depth: r = {pc2_depth_corr:.3f}, p = {pc2_depth_p:.4f} {pc2_sig}")

    sil_score = None
    if len(np.unique(groups)) > 1 and X_pca.shape[1] >= 2:
        try:
            sil_score = silhouette_score(X_pca[:, :2], groups)
            print(f"Silhouette score (PC1+PC2): {sil_score:.3f}")
        except Exception as e:
            print(f"Warning: silhouette score failed ({e})")

    feature_contributions = {}
    for pc_idx in range(min(2, len(pca.components_))):
        pc_loadings = pca.components_[pc_idx]
        top_indices = np.argsort(np.abs(pc_loadings))[-5:][::-1]

        feature_contributions[f"PC{pc_idx + 1}"] = []
        for idx in top_indices:
            size_bin = size_columns_filtered[idx]
            loading = pc_loadings[idx]
            size_bp = size_bin.replace(f"{fragment_type}_", "").replace("bp", "")
            feature_contributions[f"PC{pc_idx + 1}"].append(
                {
                    "size_bin": size_bp,
                    "loading": loading,
                    "abs_loading": abs(loading),
                }
            )

        print(f"PC{pc_idx + 1} top contributors:")
        for contrib in feature_contributions[f"PC{pc_idx + 1}"]:
            print(f"  {contrib['size_bin']}bp: {contrib['loading']:+.3f}")

    return {
        "pca": pca,
        "X_pca": X_pca,
        "X_scaled": X_scaled,
        "scaler": scaler,
        "explained_variance": explained_var,
        "cumulative_variance": cumulative_var,
        "groups": groups,
        "sample_names": sample_names,
        "group_source": group_source,
        "size_columns": size_columns_filtered,
        "stats_results": stats_results,
        "silhouette_score": sil_score,
        "feature_contributions": feature_contributions,
        "fragment_type": fragment_type,
        "sequencing_depths": sequencing_depths,
        "pc1_depth_correlation": pc1_depth_corr,
        "pc2_depth_correlation": pc2_depth_corr,
        "pc1_depth_p_value": pc1_depth_p,
        "pc2_depth_p_value": pc2_depth_p,
        "mean_fragment_sizes": mean_fragment_sizes,
    }


def create_comprehensive_plots(results):
    """Create comprehensive PCA visualization plots."""
    if not results:
        return

    fragment_type = results["fragment_type"]
    fig = plt.figure(figsize=(20, 16))

    ax1 = plt.subplot(3, 3, 1)
    unique_groups = get_ordered_groups(results["groups"])
    color_map = build_group_color_map(unique_groups)

    for group in unique_groups:
        mask = results["groups"] == group
        x_group = results["X_pca"][mask, 0]
        y_group = results["X_pca"][mask, 1]
        color = color_map.get(group, "#9467bd")

        plt.scatter(x_group, y_group, c=color, label=f"{group} (n={np.sum(mask)})", alpha=0.7, s=60)
        draw_group_circle(ax1, x_group, y_group, color)

    plt.xlabel(f"PC1 ({results['explained_variance'][0]:.1%})")
    plt.ylabel(f"PC2 ({results['explained_variance'][1]:.1%})")
    plt.title(f"{fragment_type} - PCA Scatter Plot")
    plt.legend()
    plt.grid(True, alpha=0.3)

    ax2 = plt.subplot(3, 3, 2)
    pc_numbers = range(1, len(results["explained_variance"]) + 1)
    plt.plot(pc_numbers, results["explained_variance"] * 100, "bo-", linewidth=2, markersize=6)
    plt.xlabel("Principal Component")
    plt.ylabel("Explained Variance (%)")
    plt.title(f"{fragment_type} - Scree Plot")
    plt.xticks(pc_numbers)
    plt.grid(True, alpha=0.3)

    ax2_twin = ax2.twinx()
    ax2_twin.plot(pc_numbers, results["cumulative_variance"] * 100, marker="o", linestyle="-", color="red", linewidth=2)
    ax2_twin.set_ylabel("Cumulative Variance (%)", color="red")
    ax2_twin.tick_params(axis="y", labelcolor="red")

    ax3 = plt.subplot(3, 3, 3)
    pc1_data = []
    group_labels = []
    for group in unique_groups:
        mask = results["groups"] == group
        pc1_data.append(results["X_pca"][mask, 0])
        group_labels.append(group)
    plt.boxplot(pc1_data, tick_labels=group_labels)
    plt.ylabel("PC1 Score")
    plt.title(f"{fragment_type} - PC1 Distribution")
    plt.grid(True, alpha=0.3)

    ax4 = plt.subplot(3, 3, 4)
    pc2_data = []
    for group in unique_groups:
        mask = results["groups"] == group
        pc2_data.append(results["X_pca"][mask, 1])
    plt.boxplot(pc2_data, tick_labels=group_labels)
    plt.ylabel("PC2 Score")
    plt.title(f"{fragment_type} - PC2 Distribution")
    plt.grid(True, alpha=0.3)

    ax5 = plt.subplot(3, 3, 5)
    pc1_loadings = results["pca"].components_[0, :]
    pc2_loadings = results["pca"].components_[1, :]
    size_values_loadings = np.array([int(col.replace("bp", "").split("_")[-1]) for col in results["size_columns"]])

    scatter = ax5.scatter(
        pc1_loadings,
        pc2_loadings,
        c=size_values_loadings,
        cmap="coolwarm",
        s=60,
        alpha=0.8,
        edgecolors="black",
        linewidth=0.5,
    )
    cbar = plt.colorbar(scatter, ax=ax5, shrink=0.8)
    cbar.set_label("Fragment Size (bp)", rotation=270, labelpad=20)

    plt.xlabel("PC1 Loading")
    plt.ylabel("PC2 Loading")
    plt.title(f"{fragment_type} - PC Loadings\n(Colored by Fragment Size)")
    plt.axhline(y=0, color="black", linestyle="-", alpha=0.3)
    plt.axvline(x=0, color="black", linestyle="-", alpha=0.3)
    plt.grid(True, alpha=0.3)

    if results["X_pca"].shape[1] > 2:
        ax6 = plt.subplot(3, 3, 6)
        for group in unique_groups:
            mask = results["groups"] == group
            x_group = results["X_pca"][mask, 0]
            y_group = results["X_pca"][mask, 2]
            color = color_map.get(group, "#9467bd")
            plt.scatter(x_group, y_group, c=color, label=f"{group}", alpha=0.7, s=60)
            draw_group_circle(ax6, x_group, y_group, color)

        plt.xlabel(f"PC1 ({results['explained_variance'][0]:.1%})")
        plt.ylabel(f"PC3 ({results['explained_variance'][2]:.1%})")
        plt.title(f"{fragment_type} - PC1 vs PC3")
        plt.legend()
        plt.grid(True, alpha=0.3)

    ax7 = plt.subplot(3, 3, 7)
    ax7.axis("off")

    stats_text = f"{fragment_type} Analysis Summary:\n\n"
    stats_text += f"Samples: {results['X_pca'].shape[0]}\n"
    stats_text += f"Size Bins: {len(results['size_columns'])}\n"
    stats_text += f"Group source: {results['group_source']}\n"
    stats_text += f"PC1 Variance: {results['explained_variance'][0]:.1%}\n"
    stats_text += f"PC2 Variance: {results['explained_variance'][1]:.1%}\n"
    stats_text += f"PC1+PC2 Total: {results['cumulative_variance'][1]:.1%}\n\n"

    if results["silhouette_score"] is not None:
        stats_text += f"Silhouette Score: {results['silhouette_score']:.3f}\n\n"

    stats_text += "Depth Correlations:\n"
    pc1_sig = "***" if results["pc1_depth_p_value"] < 0.001 else "**" if results["pc1_depth_p_value"] < 0.01 else "*" if results["pc1_depth_p_value"] < 0.05 else ""
